<template>
  <div class="track" @mouseover="hover = true" @mouseleave="hover = false">
    <img :src="imgUrl" loading="lazy" />

    <div class="no">
      <button v-show="focus && playable && !isPlaying" @click="playTrack">
        <svg-icon
          icon-class="play"
          style="height: 14px; width: 14px"
        ></svg-icon>
      </button>
      <span v-show="(!focus || !playable) && !isPlaying">{{ trackNo }}</span>
      <button v-show="isPlaying">
        <svg-icon
          icon-class="volume"
          style="height: 16px; width: 16px"
        ></svg-icon>
      </button>
    </div>

    <div class="title-and-artist">
      <div class="container">
        <div class="title">
          {{ trackName }}
        </div>
      </div>
    </div>
  </div>
</template>

<script>
import { mapState } from 'vuex';

export default {
  name: 'TrackListItemBili',
  props: {
    trackNo: { type: Number, required: true },
    trackProp: { type: Object, required: true },
  },
  data() {
    return {
      hover: false,
      showPlayCount: false,
      columnNumber: 5,
      gap: '44px 24px',
      playButtonSize: 22,
      listStyles: {},
    };
  },
  computed: {
    ...mapState(['settings']),
    track() {
      return this.trackProp;
    },
    playable() {
      return true;
    },
    isPlaying() {
      return this.$store.state.player.currentTrack.id === this.track?.id;
    },
    trackName() {
      return this.trackProp.name;
    },
    focus() {
      return this.hover && this.$parent.rightClickedTrack.id === 0;
    },
    imgUrl() {
      let image =
        this.trackProp.cover ??
        'https://p2.music.126.net/UeTuwE7pvjBpypWLudqukA==/3132508627578625.jpg';
      return image + '?param=224y224';
    },
  },
  created() {},
  methods: {
    playTrack() {
      this.$parent.playThisList(this.track.id);
    },
  },
};
</script>

